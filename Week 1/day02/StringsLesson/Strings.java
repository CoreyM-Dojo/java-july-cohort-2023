
public class Strings {

    // Access keyword return_type signature
    // public String helloWorld()
    
    public void hello() {
        System.out.println("Hello");
    }

    public double addNums(double a, double b) {
        System.out.println(a + b);
        return a + b;
    } 

    // Method Overloading
    public double addNums(double a, double b, double c) {
        return a + b + c;
    }




}
